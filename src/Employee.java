
public class Employee {

    String name;
    double salary;

    public double getSalary()
    {
        return this.salary;
    }

    public  String  getName()
    {
        return this.name;
    }

    public void setName(String empName)
    {
        this.name = empName;
    }

    public void setSalary(double empSalary)
    {
        this.salary = empSalary;
    }

}

